package typecasting;

public class Narrow {

	public static void main(String[] args) {

		
       long l=467546755;
       int i=(int)l;
       System.out.println(i);
       
       int a1=89;
       char c=(char)a1;
       System.out.println(c);
       
       double d=62.99d;
       int a3=(int)d;
       System.out.println(a3+"%");
	}

}
